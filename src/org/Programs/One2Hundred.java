package org.Programs;

public class One2Hundred {

	// One to Hundred without using numbers

	public static void main(String[] args) {

		int one = 'a' / 'a';

		int last = 'd';
		
		int sum = 'a';

		for (int i = one; i <= last; i++) {
			System.out.println(i);
		}

		for (int i = 'a' / 'a'; i < last; i++) {
			System.out.println(i);

		}
		System.out.println("***");
		System.out.println(sum);
		
	}

}
